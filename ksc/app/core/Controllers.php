class My_Controller extends CI_Controller {
protected $appFolder;   	
	
	public function load_class($target,$name=''){
		$aClass=explode("_",$target);
		require($this->appFolder."/".implode("/",$aClass).".php");
		if($name==''){
			$this->target = new $target;
		}else{ 
			$this->$name= new $target;
		}
	}
}